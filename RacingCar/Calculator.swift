import Foundation

struct Calculator {
    func add(_ lhs: Int, _ rhs: Int) -> Int {
        return lhs + rhs
    }
    
    func substract(_ lhs: Int, _ rhs: Int) -> Int {
        return lhs - rhs
    }
    
    func multiply(_ lhs: Int,_ rhs: Int) -> Int {
        return lhs * rhs
    }
    
    func divide(_ lhs: Int,_ rhs: Int) -> Int {
        return lhs / rhs
    }
    
}
